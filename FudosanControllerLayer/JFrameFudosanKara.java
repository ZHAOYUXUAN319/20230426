    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
          DefaultTableModel df = (DefaultTableModel) jTable1.getModel();

        int row = jTable1.getSelectedRow();
        BukenBean bukenBean = new BukenBean();
        RealEstateCompanyBean realEstateCompanyBean = new RealEstateCompanyBean();
        realEstateCompanyBean.setCompanyId(Integer.parseInt(df.getValueAt(row, 0).toString()));
        BukenService list = new BukenService();
        DefaultTableModel dff = (DefaultTableModel) jTable2.getModel();
        //強制的に文字列に変更する
        List<BukenBean> listinfo = list.fudosanheya(String.valueOf(realEstateCompanyBean.getCompanyId()));
        for (BukenBean c : listinfo) {
            dff.addRow(new Object[]{c.getPropertyId(), c.getPropertyName(), c.getAddress(), c.getPropertyType(), c.getPropertyArea(), c.getPrice(),c.getSyozokuCompanyId()});
        }
    }//GEN-LAST:event_jButton3ActionPerformed

