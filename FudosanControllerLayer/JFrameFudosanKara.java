    public void getlistInfoFudosan() {

        BukenService list = new BukenService();
        List<RealEstateCompanyBean> listinfo = list.listInfoFudosan();
        DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
        df.setRowCount(0);
        for (RealEstateCompanyBean c : listinfo) {
            df.addRow(new Object[]{c.getCompanyId(), c.getCompanyName(), c.getAddress(), c.getPhoneNumber()});
        }
    }

